package bean;

public class Model {
	
	private String modelId;
	private String modelName;
	private String modelDescription;
	private String modelFeatures;
	private double modelPrice;
	private int thresholdLimit;
	
		public String getModelId() {
		return modelId;
	}
	public void setModelId(String modelId) {
		this.modelId = modelId;
	}
	public String getModelName() {
		return modelName;
	}
	public void setModelName(String modelName) {
		this.modelName = modelName;
	}
	public String getModelDescription() {
		return modelDescription;
	}
	public void setModelDescription(String modelDescription) {
		this.modelDescription = modelDescription;
	}
	public String getModelFeatures() {
		return modelFeatures;
	}
	public void setModelFeatures(String modelFeatures) {
		this.modelFeatures = modelFeatures;
	}
	public double getModelPrice() {
		return modelPrice;
	}
	public void setModelPrice(double modelPrice) {
		this.modelPrice = modelPrice;
	}
	public int getThresholdLimit() {
		return thresholdLimit;
	}
	public void setThresholdLimit(int thresholdLimit) {
		this.thresholdLimit = thresholdLimit;
	}
	
	
}
